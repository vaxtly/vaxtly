<h2 class="text-xl font-bold text-gray-900 dark:text-white border-b border-gray-200 dark:border-gray-700 pb-2 mb-4">Getting Started</h2>

<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed mb-4">
    Vaxtly is a desktop API testing tool built for developers. It lets you create, organize, and execute HTTP requests with support for environments, variables, scripting, Git-based sync, HashiCorp Vault integration, and more.
</p>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Quick Tour</h3>
<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 mb-6">
    <li><strong class="text-gray-700 dark:text-gray-300">Sidebar</strong> — Browse and manage your collections, folders, requests, and environments. Toggle between Collections and Environments views at the top.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Tab Bar</strong> — Open multiple requests in tabs. The active environment selector and lock button are on the right side.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Request Builder</strong> — Configure method, URL, headers, query params, body, and authentication for each request.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Response Viewer</strong> — See status code, timing, formatted response body (JSON is syntax-highlighted), and response headers.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">System Log</strong> — Expandable panel at the bottom showing request history and sync/vault operation logs.</li>
</ul>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Layout & Appearance</h3>
<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 mb-6">
    <li><strong class="text-gray-700 dark:text-gray-300">Layout mode</strong> — Toggle between side-by-side (columns) and stacked (rows) using the layout button in the bottom-left of the request builder.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Dark mode</strong> — Toggle light/dark theme from the icon at the bottom of the sidebar.</li>
</ul>

