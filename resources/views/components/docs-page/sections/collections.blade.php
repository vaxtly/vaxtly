<h2 class="text-xl font-bold text-gray-900 dark:text-white border-b border-gray-200 dark:border-gray-700 pb-2 mb-4">Collections & Folders</h2>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">What Are Collections?</h3>
<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed mb-4">
    Collections are logical groups of related API requests — think of them like projects. Each collection can hold requests, folders (with unlimited nesting), and its own set of variables.
</p>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Managing Collections</h3>
<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 mb-6">
    <li><strong class="text-gray-700 dark:text-gray-300">Create</strong> — Click the <code class="px-1.5 py-0.5 bg-gray-100 dark:bg-gray-700 rounded text-xs font-mono text-pink-600 dark:text-pink-400">+</code> button at the top of the sidebar.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Rename</strong> — Right-click a collection (or click the 3-dot menu) and choose Rename.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Delete</strong> — Right-click a collection and choose Delete. This deletes all folders and requests within it.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Duplicate requests</strong> — Right-click a request and choose Duplicate to clone it in the same folder.</li>
</ul>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Folders</h3>
<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed mb-2">
    Organize requests into folders with unlimited nesting depth. Folders help you group related endpoints together.
</p>
<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2 mb-6">
    <li><strong class="text-gray-700 dark:text-gray-300">Create</strong> — Right-click a collection or folder and choose <strong class="text-gray-700 dark:text-gray-300">Add subfolder</strong>.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Rename</strong> — Right-click a folder and choose Rename.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Add requests</strong> — Right-click a folder and choose <strong class="text-gray-700 dark:text-gray-300">Add request</strong>.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Set environments</strong> — Right-click a folder and choose <strong class="text-gray-700 dark:text-gray-300">Set environments</strong> to associate environments with that folder. See <strong class="text-gray-700 dark:text-gray-300">Environments > Folder Environments</strong> for details.</li>
</ul>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Collection Variables</h3>
<p class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed mb-4">
    Each collection has its own key-value variables (editable in the collection settings). Both collection and environment variables are available in requests. If the same key exists in both, the <strong class="text-gray-700 dark:text-gray-300">environment variable takes precedence</strong>.
</p>

<h3 class="text-base font-semibold text-gray-800 dark:text-gray-200 mb-2">Search & Sort</h3>
<ul class="text-sm text-gray-600 dark:text-gray-400 leading-relaxed space-y-2">
    <li><strong class="text-gray-700 dark:text-gray-300">Search</strong> — Use the search bar at the top of the sidebar to filter across collection names, folder names, request names, and URLs.</li>
    <li><strong class="text-gray-700 dark:text-gray-300">Sort</strong> — Sort collections and requests by name (A-Z, Z-A), newest, or oldest.</li>
</ul>
